# 내 풀이 1 -> 효율성테스트 시간초과
def solution(people, limit):
    p = sorted(people, reverse = True)
    i, cnt = 0, 0
    
    while len(p):
        x = p.pop()
        for i in range(len(p)):
            if p[i] + x <= limit:
                p.pop(i)
                cnt += 1
                break
        else:
            cnt += 1        

    return cnt

