def solution2(people, limit):
    people.sort()                           # 정렬
    x = 0                                                                     
    y = len(people) - 1                   
    cnt = 0                                 # 2명이 탑승한 보트 수 카운트
    
    while x < y:                            # x가 y보다 커지면 stop
        if people[x] + people[y] <= limit:  # 몸무게 합이 limit보다 작거나 같으면 카운트+1, x+1
            cnt += 1
            x += 1
        y -= 1                              # y는 1씩 줄여나가면서 탐색
    
    return len(people) - cnt                # 총 사람 수 - 2명이 탑승한 보트의 수

